# luas dan keliling jajar genjang

alas = float(input("Masukan alas jajar genjang : "))
tinggi = float(input("Masukan tinggi jajar genjang : "))
sisi_miring = float(input("Masukan sisi miring jajar genjang : "))

luas = alas * tinggi
keliling = 2 * (alas + sisi_miring)

print(f"Luas jajar genjang adalah : {luas}")
print(f"Keliling jajar genjang adalah : {keliling}")    

